weather = "fine"

case weather
when "fine"
    puts "Go out and enjoy the sunshine!"
when "hot"
    puts "Stay inside with the air conditioning!"
when "rainy"
    puts "Find your umbrella!"
when "foggy"
    puts "Perhaps it will clear up later!"
when "cloudy"
    puts "Best keep an eye on it!"
else
    puts "It's sure to change later!"
end